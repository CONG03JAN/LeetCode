from typing import List
import queue

class Solution:
    def numIslands(self, grid: List[List[str]]) -> int:

        if len(grid) == 0: return 0
        m = len(grid)
        n = len(grid[0])

        flag = [[False for _ in range(n)] for _ in range(m)] 
        directions = [[-1, 0], [0, 1], [1, 0], [0, -1]]

        count = 0

        for i in range(m):
            for j in range(n):
                if grid[i][j] == "1" and not flag[i][j]:
                    count += 1
                    q = queue.Queue()
                    q.put([i, j])
                    flag[i][j] = True
                    while not q.empty():
                        tmp = q.get()
                        for d in directions:
                            x = tmp[0] + d[0]
                            y = tmp[1] + d[1]
                            if 0 <= x < m and 0 <= y < n and not flag[x][y] and grid[x][y] == "1":
                                q.put([x, y])
                                flag[x][y] = True
        return count



